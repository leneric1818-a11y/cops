"""Compute Inter-Rater Reliability (IRR) for the human-evaluation v3 sample.

Reads:
  outputs/human_eval/v3/human_eval_sample.json
      Gold mapping: per-item judge_winner, per_provider_winners, aa_delta,
      stratum, n_agree, axis, target_pole, base_is_response_a.
  outputs/human_eval/v3/studio_export.json
      LLARS Studio export (scenario 442 _final): per (rater, item) A/B/tie.

Emits:
  outputs/human_eval/v3/irr_summary.json
      Machine-readable summary of every metric computed below.
  paper/tables/tab_irr_headline.tex
  paper/tables/tab_irr_stratum.tex
  paper/tables/tab_irr_axis.tex
  paper/tables/tab_irr_nagree.tex
      Auto-generated LaTeX tables consumed by §6.3 and Appendix A.

Reliability raters considered:
  Four primary humans (RaterA, RaterB, RaterD, RaterC)
  and the three provider judges (Magistral, Nano, GPT-OSS-120B). All
  four humans annotated essentially the full 124-item sample
  (coverage 90--100%); 110 items were rated by all four and form the
  joint-subset for Fleiss's κ and human-majority statistics.

Implements Cohen's kappa, Fleiss's kappa, and Pearson r in pure Python
without scipy. Run from repo root with:
  .venv/bin/python scripts/compute_irr.py
"""

